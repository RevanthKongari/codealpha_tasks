    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int bleave = 0;
        LoadLeave();
        String empno = jComboBox1.getSelectedItem().toString();
        int days=Integer.parseInt(jSpinner1.getValue().toString());
        if(jRadioButton1.isSelected()==true)
        
        {
            bleave = casual - days ;
            if(bleave < 0){
                JOptionPane.showMessageDialog(null, "You have only "+casual+" casual leaves");                 
            }
            else
            {
                Connec o1=new Connec();
                try {
                    o1.stm.executeUpdate("update Leaveinfo set casual='"+bleave+"' where empnum='"+empno+"' ");
                    JOptionPane.showMessageDialog(null, "Your casual leave is granted");
                } catch (SQLException ex) {
                    Logger.getLogger(LeaveCal.class.getName()).log(Level.SEVERE, null, ex);
                    ex.printStackTrace();
                }
            }
        } 
        else if(jRadioButton2.isSelected()==true)
        {
            bleave = annual - days;
            if(bleave < 0){
                JOptionPane.showMessageDialog(null, "You have only "+annual+" annual leaves");                 
            }
            else{
                Connec o2=new Connec();
                try {
                    o2.stm.executeUpdate("update Leaveinfo set annual='"+bleave+"' where empnum='"+empno+"' ");
                    JOptionPane.showMessageDialog(null, "Your annual leave is granted");
                } catch (SQLException ex) {
                    Logger.getLogger(LeaveCal.class.getName()).log(Level.SEVERE, null, ex);
                    ex.printStackTrace();
                }
            }            
        }
        else if(jRadioButton3.isSelected()==true){
            bleave = medical - days;
            if(bleave < 0){
                JOptionPane.showMessageDialog(null, "You have only "+medical+" medical leaves");                 
            }
            else{
                Connec o3=new Connec();
                try {
                    o3.stm.executeUpdate("update Leaveinfo set medical='"+bleave+"' where empnum='"+empno+"' ");
                    JOptionPane.showMessageDialog(null, "Your medical leave is granted");
                } catch (SQLException ex) {
                    Logger.getLogger(LeaveCal.class.getName()).log(Level.SEVERE, null, ex);
                    ex.printStackTrace();
                }
            }            
        }
        else{
            JOptionPane.showMessageDialog(null, "invalid operation performed"); 
        }
        setVisible(false);
        new LeaveCal().setVisible(true);
       
    }//GEN-LAST:event_jButton1ActionPerformed

