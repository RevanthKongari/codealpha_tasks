    public void LoadEmpno(){
        try {
            Connec obj=new Connec();
            ResultSet rs= obj.stm.executeQuery("select * from registration");
            jComboBox1.removeAllItems();
            while(rs.next()){
                jComboBox1.addItem(rs.getString(1));
            }
        } catch (SQLException ex) {
            Logger.getLogger(LeaveCal.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }
    public void LoadLeave()
    {
        
        String empno = jComboBox1.getSelectedItem().toString();
        try{
            Connec o=new Connec();
            ResultSet s=o.stm.executeQuery("select * from Leaveinfo where empnum='"+empno+"' ");
            if(s.next()==false){
                JOptionPane.showMessageDialog(null, "Error"); 
            }  
            else{
                casual = s.getInt("casual");
                annual = s.getInt("annual");
                medical = s.getInt("medical");
            }
        }
        catch(Exception Ex){
            Ex.printStackTrace();
        }
        
    }

